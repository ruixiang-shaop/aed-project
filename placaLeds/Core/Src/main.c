//Definir el callback para cuando se reciben datos en I2C como esclavo
void HAL_I2C_SlaveRxCpltCallback(I2C_HandleTypeDef *hi2c)
{
  uint8_t tipoMensaje = (i2cData >> 0 ) & 0x3; // quedarse con los dos últimos bits del final

  uint8_t idLed = (i2cData >> 2) & 0x7; // Quedarse con los tres bits después de los dos primeros del final
  uint8_t onOff = (i2cData >> 5) & 0x1; // Quedarse con el bit después de los 5 primeros del final

  uint8_t fxId = (i2cData >> 2) & 0x3; // Quedarse con los dos bits después de los dos primeros del final

  switch (tipoMensaje) {
    case 0x0:
      /* Single led */
      HAL_GPIO_WritePin(LD1_GPIO_Port, LD1_Pin, GPIO_PIN_RESET);
      HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);
      HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, GPIO_PIN_RESET);
      HAL_GPIO_WritePin(LEDS_GPIO_Ports[idLed], LEDS_Pins[idLed], onOff);
      break;
    case 0x1:
      /* Efecto */
      HAL_GPIO_WritePin(LD1_GPIO_Port, LD1_Pin, GPIO_PIN_RESET);
      HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);
      HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, GPIO_PIN_RESET);
      efectoActual = fxId;
      break;
    case 0x3:
      /* Alarma */
      HAL_GPIO_WritePin(LED1_GPIO_Port, LED1_Pin, GPIO_PIN_RESET);
      HAL_GPIO_WritePin(LED2_GPIO_Port, LED2_Pin, GPIO_PIN_RESET);
      HAL_GPIO_WritePin(LED3_GPIO_Port, LED3_Pin, GPIO_PIN_RESET);
      HAL_GPIO_WritePin(LED4_GPIO_Port, LED4_Pin, GPIO_PIN_RESET);
      HAL_GPIO_WritePin(LED5_GPIO_Port, LED5_Pin, GPIO_PIN_RESET);
      HAL_GPIO_WritePin(LD1_GPIO_Port, LD1_Pin, GPIO_PIN_SET);
      HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_SET);
      HAL_GPIO_WritePin(LD3_GPIO_Port, LD3_Pin, GPIO_PIN_SET);
      efectoActual = 0xFF;
      etapa = 0;
      HAL_Delay(1000);
      break;
    default:
      break;
  }


}


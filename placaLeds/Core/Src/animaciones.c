#include "animaciones.h"

void avanzar_etapa()
{
  switch (efectoActual) {
    case 0:
      etapa = (etapa + 1) % 5;
      break;
    case 1:
      etapa = (etapa + 1) % 5;
      break;
    case 2:
      etapa = (etapa + 1) % 3;
      break;
    case 3:
      etapa = (etapa + 1) % 3;
      break;

    default:
      break;
  }
}

void animaciones()
{
  switch (efectoActual) {
    case 0:
      animacion0();
      break;
    case 1:
      animacion1();
      break;
    case 2:
      animacion2();
      break;
    case 3:
      animacion3();
      break;

    default:
      break;
  }
}

